from opentrons import containers, instruments

# a tip rack for our pipette, in Bay B1
tiprack = containers.load('tiprack-200ul', 'B1')

# transfer from this 96wells plate, in bay CB2
plateA = containers.load('96-flat', 'B2')

# destination 96wells plate, in Bay C2
plateB = containers.load('96-flat', 'C2')

# a trash container for the tips
trash = containers.load('trash-box', 'A1')

# a 100uL pipette used for the transfer
pipette = instruments.Pipette(
    axis='a',
    name='my-p100-single',
    min_volume=10,
    max_volume=100,
    trash_container=trash,
    tip_racks=[tiprack]
)

# Transfer 96 samples from plateA to plateB, the volume is 100uL and change tips between
# each transfer:

